<#
.SYNOPSIS
    Creates a new GitHub Gist.

.DESCRIPTION
    The New-GithubGist cmdlet creates a new GitHub Gist from either raw content
    or an existing local file. It supports both public and private gists and
    returns a PSCustomObject containing details about the newly created Gist.

.PARAMETER GistContent
    The content to include in the Gist. Required when using the Content parameter set.

.PARAMETER GistFileName
    The file name to assign within the Gist. Required when using the Content parameter set.

.PARAMETER PersonalAccessToken
    The GitHub personal access token for authentication. Aliases: PAT, Token.

.PARAMETER Filename
    The path to a local file whose contents will be uploaded to the Gist.
    Required when using the FileName parameter set.

.PARAMETER Description
    A short description of the Gist. Default value: "Created by PSScriptUpdater".

.PARAMETER Visibility
    The visibility of the Gist. Valid values are 'Public' or 'Private'.
    Default value: 'Public'.

.INPUTS
    None. This cmdlet does not accept pipeline input.

.OUTPUTS
    PSCustomObject with the following properties:
      • GistId       – The ID of the created Gist.
      • GistFileName – The name of the file in the Gist.
      • URL          – The HTML URL for viewing the Gist.
      • RawUrl       – The raw content URL of the Gist file.
      • Visibility   – The visibility status ('Public' or 'Private').

.EXAMPLE
    # Create a public Gist from inline content
    New-GithubGist -GistContent "Hello, world!" `
                   -GistFileName "hello.txt" `
                   -PersonalAccessToken "YOUR_TOKEN"

.EXAMPLE
    # Create a private Gist from an existing script file
    New-GithubGist -Filename "C:\Scripts\MyScript.ps1" `
                   -PersonalAccessToken "YOUR_TOKEN" `
                   -Visibility Private

.NOTES
    Requires PowerShell 5.1+ and a valid GitHub Personal Access Token
    with gist scope permissions.
#>
function New-GithubGist {
    [CmdletBinding(DefaultParameterSetName = 'Content')]
    param (
        [Parameter(Mandatory = $true, ParameterSetName = 'Content')]
        [ValidateNotNullOrEmpty()]
        [string]$GistContent,

        [Parameter(Mandatory = $true, ParameterSetName = 'Content')]
        [ValidateNotNullOrEmpty()]
        [string]$GistFileName,

        [Parameter(Mandatory = $true, ParameterSetName = 'Content')]
        [Parameter(Mandatory = $true, ParameterSetName = 'FileName')]
        [ValidateNotNullOrEmpty()]
        [Alias('PAT', 'Token')]
        [string]$PersonalAccessToken,

        [Parameter(Mandatory = $true, ParameterSetName = 'FileName')]
        [ValidateNotNullOrEmpty()]
        [string]$Filename,

        [Parameter(Mandatory = $false, ParameterSetName = 'Content')]
        [Parameter(Mandatory = $false, ParameterSetName = 'FileName')]
        [string]$Description = "Created by PSScriptUpdater",

        [Parameter(Mandatory = $true, ParameterSetName = 'Content')]
        [Parameter(Mandatory = $true, ParameterSetName = 'FileName')]
        [ValidateSet('Public', 'Private')]
        [String]$Visibility = 'Public'
    )

    if ($PSCmdlet.ParameterSetName -eq 'FileName') {
        Write-Verbose -Message "Reading content from file: $Filename"
        if (-not (Test-Path -Path $Filename)) {
            Write-Error -Message "The specified file '$Filename' does not exist."
            return $null
        }
        $GistContent = Get-Content -Path $Filename -Raw
        $GistFileName = [System.IO.Path]::GetFileName($Filename)
        Write-Verbose -Message "File content (from $Filename) read successfully."
    }

    # Define the API endpoint for creating a Gist
    $apiUrl = "https://api.github.com/gists"
    $body = @{
        description = $Description
        files       = @{
            "$($gistFileName)" = @{
                content = $GistContent
            }
        }
    }
    if ($Visibility -eq 'Private') {
        $body.public = $false
    }
    if ($Visibility -eq 'Public') {
        $body.public = $true
    }

    $bodyJson = $body | ConvertTo-Json -Compress -Depth 6

    Write-Host -Message "Creating Gist with file name: $GistFileName"
    $headers = @{
        Authorization = "Bearer $($PersonalAccessToken)"
        Accept        = "application/vnd.github+json"
    }
    try {
        Write-Verbose -Message "Creating new Gist with file name: $GistFileName"
        $response = Invoke-RestMethod -Uri $apiUrl -Method Post -Headers $headers -Body $bodyJson -ErrorAction Stop
        Write-Verbose -Message "Gist created successfully: $($response.html_url)"
        $rawUrl = $response.files.$gistFileName.raw_url
        $htmlUrl = $response.html_url
        $gistVisibility = if ($response.public) { 'Public' } else { 'Private' }
        return [PSCustomObject]@{
            GistId       = $response.id
            GistFileName = $GistFileName
            URL          = $htmlUrl
            RawUrl       = $rawUrl
            Visibility   = $gistVisibility
        }
    } catch {
        Write-Error -Message "Failed to create Gist: $($_.Exception.Message)"
        return $null
    }
}

